    private void btnAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirActionPerformed
        JFileChooser ruta = new JFileChooser();
        ruta.setFileFilter(filtroRuta);
        int opc = ruta.showOpenDialog(this);
        
        if(opc == JFileChooser.APPROVE_OPTION){
            txtRuta.setText(ruta.getSelectedFile().getPath());
            
        try {
            //Automata AFN
            txtAutomata.setText(CArchivo.leerTxt(txtRuta.getText()));
            
            //Obtener parte de la Quintupla AFN
            txtEstadosAFN.setText(CArchivo.caracter(CArchivo.linea(0, txtRuta.getText())));
            txtAlfabetoAFN.setText(CArchivo.caracter(CArchivo.linea(1, txtRuta.getText())));
            txtAceptacionAFN.setText(CArchivo.caracter(CArchivo.linea(3, txtRuta.getText())));
            
        } catch (IOException ex) {
            Logger.getLogger(FormularioAutomata.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //Envio de datos al constructor
        AFN_AFD.setW_AFN(CArchivo.caracter(CArchivo.linea(4, txtRuta.getText())).split("\n"));
        AFN_AFD.setSo(CArchivo.caracter(CArchivo.linea(2, txtRuta.getText())).replaceAll("\n", ""));
        AFN_AFD.setEstados(txtEstadosAFN.getText().split("\n"));
        AFN_AFD.setAlfabeto(txtAlfabetoAFN.getText().split("\n"));
        
        //Matriz AFN
        String [][] MAutomataAFN = new String [txtEstadosAFN.getText().split("\n").length+1][txtAlfabetoAFN.getText().split("\n").length+2];
        String [][] MAutomataAFD;      
        MAutomataAFN = AFN_AFD.AFN(MAutomataAFN);
        tablaAFN(MAutomataAFN, txtEstadosAFN.getText().split("\n"), txtAlfabetoAFN.getText().split("\n")); 
        
        //Matriz AFD
        MAutomataAFD = AFN_AFD.AFD();   
        tablaAFD(MAutomataAFD, MAutomataAFD.length, txtAlfabetoAFN.getText().split("\n")); 
        
        //obtener parte de la quintupla AFD
        txtEstadosAFD.setText(AFN_AFD.EstadosAFD(MAutomataAFD));
        txtAlfabetoAFD.setText(txtAlfabetoAFN.getText());       
        txtAceptacionAFD.setText(AFN_AFD.Aceptacion(MAutomataAFD, txtAceptacionAFN.getText().split("\n")));
        
        AFN_AFD.setW_AFD(AFN_AFD.FunTransicionAFD(MAutomataAFD, txtEstadosAFD.getText().split("\n")).split(","));
        
        }else{
            JOptionPane.showMessageDialog(null, "NO SELECCIONO EL AUTOMATA","ERROR",3);
        }
        
    }//GEN-LAST:event_btnAbrirActionPerformed

    
    public void tablaAFN(String[][] Matriz, String[] estad, String[] alfab){
        
        DefaultTableModel tablaAFN = (DefaultTableModel) tblAFN.getModel();
        
        tblAFN.getTableHeader().setUI(null);
        int filas = estad.length+1;
        int col = alfab.length+2;
        tablaAFN.setRowCount(filas);
        tablaAFN.setColumnCount(col);
        
        //
        for(int i=0; i<filas; i++){
            for(int j=0; j<col; j++){
                tblAFN.setValueAt(Matriz[i][j], i, j);
            }                
        } 
    }
    
    public void tablaAFD(String[][] Matriz, int nEst, String[] alfabeto){
        
        DefaultTableModel tablaAFD = (DefaultTableModel) tblAFD.getModel();
        
        tblAFD.getTableHeader().setUI(null);       
        int col = alfabeto.length+2;
        tablaAFD.setRowCount(nEst);
        tablaAFD.setColumnCount(col);
        
        for(int i=0; i<nEst; i++){
            for(int j=0; j<col; j++){
                tblAFD.setValueAt(Matriz[i][j], i, j);
            }                
        } 
    } 
    
