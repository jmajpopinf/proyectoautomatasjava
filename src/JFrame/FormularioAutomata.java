    private void btnEvaluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvaluarActionPerformed
        try {
            String[][] DTransicion = AFN_AFD.AFD(); 
            if(txtEvaluar.getText().equals("")){
                JOptionPane.showMessageDialog(null, "INGRESE UNA CADENA A EVALUAR","ERROR",3);       
            }else{
                txtResultado.setText(AFN_AFD.evalCadena(txtEvaluar.getText(),DTransicion, txtAceptacionAFD.getText().replaceAll("\n", "")));
            } 
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO HAY AUTOMATA CARGADO","ERROR",3);
        } 
        
    }//GEN-LAST:event_btnEvaluarActionPerformed

